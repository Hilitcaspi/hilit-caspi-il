// Score answers for spider chart
function scoreForSpider(answers: Record<string, string>): { current: number[]; desired: number[] } {
  const fearScore = answers.m1_fear ? (answers.m1_fear === "rejection" ? 3 : answers.m1_fear === "abandonment" ? 2 : answers.m1_fear === "disappointment" ? 4 : 5) : 3;
  const patternScore = answers.m1_pattern ? (answers.m1_pattern === "self_sabotage" ? 2 : answers.m1_pattern === "wrong_people" ? 3 : answers.m1_pattern === "over_invest" ? 4 : 3) : 3;
  const attachScore = answers.m2_attachment ? (answers.m2_attachment === "secure" ? 9 : answers.m2_attachment === "confused" ? 4 : answers.m2_attachment === "anxious" ? 5 : 6) : 5;
  const commScore = answers.m4_horseman ? (answers.m4_horseman === "contempt" ? 3 : answers.m4_horseman === "criticism" ? 5 : answers.m4_horseman === "defensiveness" ? 6 : 5) : 5;
  const loveScore = (answers.m3_receive && answers.m3_give) ? 8 : answers.m3_receive ? 6 : 4;

  return {
    current: [fearScore, patternScore, attachScore, commScore, loveScore],
    desired: [9, 9, 9, 9, 9],
  };
}

// ─── Device fingerprint helper ───────────────────────────────────────────────
function getDeviceFingerprint(): string {
  const stored = localStorage.getItem("hilit_device_fp");
  if (stored) return stored;
  const raw = [
    navigator.userAgent,
    navigator.language,
    screen.width + "x" + screen.height,
    Intl.DateTimeFormat().resolvedOptions().timeZone,
    navigator.hardwareConcurrency ?? 0,
  ].join("|");
  let hash = 0;
  for (let i = 0; i < raw.length; i++) {
    hash = ((hash << 5) - hash) + raw.charCodeAt(i);
    hash |= 0;
  }
  const fp = Math.abs(hash).toString(16).padStart(8, "0") + "-" + Date.now().toString(16);
  localStorage.setItem("hilit_device_fp", fp);
  return fp;
}

